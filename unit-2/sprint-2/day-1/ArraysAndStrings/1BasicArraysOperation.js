// ********** Arrays **********

// Array using constructor function
let num = new Array(20, 40, 10, 50);
// console.log(num);

// Using Array Literal
let nums = [10, 43, 25, 61];
// console.log(nums);

// push() add element to the last of array 
nums.push(70);
nums.push(5, 7, 6);
nums.push([11]);
// console.log(nums);

// unshift() add to the starting in an array
nums.unshift(1);
nums.unshift(200, 500);
// console.log(nums);

// pop() removes last element from the array
// shift() removes starting element from the array
nums.pop();
nums.shift()
console.log(nums);